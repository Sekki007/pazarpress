CREATE TABLE IF NOT EXISTS restaurants (
  id VARCHAR(64) PRIMARY KEY,
  ownerId VARCHAR(64) NULL,
  name VARCHAR(190) NOT NULL,
  slug VARCHAR(220) NOT NULL UNIQUE,
  city VARCHAR(64) NOT NULL,
  address TEXT NULL,
  phone VARCHAR(64) NULL,
  whatsapp VARCHAR(64) NULL,
  description TEXT NULL,
  logoImage TEXT NULL,
  coverImage TEXT NULL,
  hoursJson JSON NULL,
  menuLangsJson JSON NULL,
  status VARCHAR(32) NOT NULL DEFAULT 'PENDING',
  qrCode VARCHAR(64) NULL,
  reviewsEnabled TINYINT(1) NOT NULL DEFAULT 1,
  avgRating DECIMAL(3,1) NULL,
  reviewCount INT NOT NULL DEFAULT 0,
  viewCount INT NOT NULL DEFAULT 0,
  publishedAt DATETIME NULL,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);
CREATE TABLE IF NOT EXISTS menu_categories (
  id VARCHAR(64) PRIMARY KEY,
  restaurantId VARCHAR(64) NOT NULL,
  name VARCHAR(190) NOT NULL,
  translationsJson JSON NULL,
  sortOrder INT NOT NULL DEFAULT 0,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  CONSTRAINT fk_menu_categories_rest FOREIGN KEY (restaurantId) REFERENCES restaurants(id) ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS menu_items (
  id VARCHAR(64) PRIMARY KEY,
  categoryId VARCHAR(64) NOT NULL,
  restaurantId VARCHAR(64) NOT NULL,
  name VARCHAR(190) NOT NULL,
  description TEXT NULL,
  price DECIMAL(10,2) NULL,
  priceLabel VARCHAR(190) NULL,
  currency VARCHAR(8) NOT NULL DEFAULT 'RSD',
  image TEXT NULL,
  tagsJson JSON NULL,
  translationsJson JSON NULL,
  isAvailable TINYINT(1) NOT NULL DEFAULT 1,
  sortOrder INT NOT NULL DEFAULT 0,
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  CONSTRAINT fk_menu_items_cat FOREIGN KEY (categoryId) REFERENCES menu_categories(id) ON DELETE CASCADE,
  CONSTRAINT fk_menu_items_rest FOREIGN KEY (restaurantId) REFERENCES restaurants(id) ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS restaurant_reviews (
  id VARCHAR(64) PRIMARY KEY,
  restaurantId VARCHAR(64) NOT NULL,
  name VARCHAR(190) NOT NULL,
  rating INT NOT NULL,
  body TEXT NULL,
  status VARCHAR(32) NOT NULL DEFAULT 'PENDING',
  createdAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT fk_rest_reviews_rest FOREIGN KEY (restaurantId) REFERENCES restaurants(id) ON DELETE CASCADE
);
CREATE TABLE IF NOT EXISTS auto_vesti_settings (
  settingKey VARCHAR(128) PRIMARY KEY,
  value LONGTEXT NOT NULL,
  updatedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);
